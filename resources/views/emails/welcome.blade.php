</p>
<p>
    The Clubhouse is the system the Rangers use for signing up for training and
    shifts and dealing with other bits of administrivia.
</p>
<p>
    To get to the Secret Clubhouse go to this link:<br>
     <a href="https://ranger-clubhouse.burningman.org">ranger-clubhouse.burningman.org</a>
</p>
<p>
    <strong>Things to do ASAP, Now, Yesterday, Immediately:</strong>
</p>

<p>
    1. <strong>Update your password and verify your information in the Ranger
    Secret Clubhouse.</strong> Navigate to the Secret Clubhouse at <a
    href="https://ranger-clubhouse.burningman.org/">ranger-clubhouse.burningman.org/</a>,
    and click the “Forgot your password?” button. Then enter the email address
    at which you received this message and click the "Reset Password" button.
    You will be sent a temporary password. Then, log in to set up your permanent
    password.
</p>
<p>
    While you are in the Secret Clubhouse, please check all the information that
    we have for you. You can find "Personal Info" and "Emergency Contact" in
    menu at the top, or the sidebar on the left (desktop &amp; tablets).
    Please make sure that your address, phone number(s), and emergency contact info are all
    correct. Click the “Update” buttons on each page to save your changes
</p>
<p>
    2. <strong>Upload a BMID photo.</strong> Before you can sign up for training
    you’ll need to upload a photo for your Burning Man ID badge. Before you
    select/take your photo, please read the photo guidelines here:
    <a href="http://bit.ly/2G8gROk">bit.ly/2G8gROk</a>.
</p>
<p>
    Log in to the Ranger Secret Clubhouse and click "Upload a New Photo" (on the
    right side of the home page on desktop). Follow the instructions in Lambase.
    Note that it may take 2-3 days for your BMID photo to be approved (they can
    be pretty picky). If you encounter any problems, please email our fantastic
    BMID experts at <a href="mailto:ranger-vc-list@burningman.org">ranger-vc-list@burningman.org</a>
</p>
<p>
    3. <strong>Read the Ranger manual and complete the manual review.</strong>
    Another thing you must do before you can sign up for training is to read the
    most recent version of the Ranger Manual (links on
    <a href="https://rangers.burningman.org/">rangers.burningman.org</a>
    at the bottom on mobile, in the right sidebar
    on desktop). Once you've read the manual, go to "Schedule / Sign Up" in the
    Clubhouse to complete the manual review.
</p>
<p>
    4. <strong>Sign up for a training.</strong> Once you have completed the
    other steps, go to “Schedule / Sign Up”, and pick a training that works for
    you. Ranger Training locations are posted at this link:
    <a href="http://rangers.burningman.org/training">rangers.burningman.org/training</a>
    and will continue to be updated through the end of May, as new trainings are
    announced. Stay tuned to New Volunteer Announce as new trainings are posted
    Please sign up for a training by June 15th, as they fill up. There are no
    walk-ins for trainings, so make sure you sign up in the Clubhouse.
</p>
<p>
    Note that to sign up for an Alpha shift, you will need to first pass
    training. Taking the Review AND uploading a BMID photo are prerequisites for
    signing up for Training, and Training is a prerequisite for Mentoring.
</p>
