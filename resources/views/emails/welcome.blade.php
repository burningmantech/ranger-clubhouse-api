<p>
  Welcome to the Black Rock Rangers Secret Clubhouse!
</p>
<p>
  The Clubhouse is the system the Rangers use for signing up for training and shifts and dealing with other bits of administrivia.
</p>
<p>
  To get to the Secret Clubhouse go to this link: <a href="https://ranger-clubhouse.burningman.org">https://ranger-clubhouse.burningman.org</a>
</p>
<p>
  Your next steps:
</p>
<p>
  1. <b>Update your password and verify your information in the Ranger Secret
  Clubhouse.</b> Log in to the Secret Clubhouse at
  <a href="https://ranger-clubhouse.burningman.org">https://ranger-clubhouse.burningman.org</a>
  using the email address at which you received this email, and click the "Email
  Me a New Password" button. You will be sent a temporary password. Then, log in
  again to set up your permanent password.
</p>
<p>
  While you are in the Secret Clubhouse, please check all the information that
  we have for you. Go to "Me" in the menu again and then click "My personal
  info." Please make sure that your address, phone number(s), and emergency
  contact info are correct. Click the "Save changes" button to save your
  changes.
</p>
<p>
  2. <b>Upload a BMID photo.</b> Before you can sign up for training you'll need to
  upload a Burning Man ID ("BMID") photo for your Ranger ID badge. Please read
  the photo guidelines here: <a href="http://bit.ly/2G8gROk">http://bit.ly/2G8gROk</a>
</p>
<p>
  Then log in to the Ranger Secret Clubhouse and follow the instructions there.
 Note that it may take 2-3 days for your BMID photo to be approved (they can be
 pretty picky). If you encounter any problems, please email our fantastic BMID
 expert, the vivacious Miss Piggy, at <a href="mailto:misspiggy@burningman.org">misspiggy@burningman.org</a>.
</p>
<p>
  3. <b>Read the Ranger Manual and take the Manual Review.</b> Another thing you must
  do before you can sign up for training is to read the most recent version of
  the Ranger Manual and take (and pass) the Manual Review. We are still
  finalizing the 2018 Ranger Manual and Manual Review for this year, so watch
  your email for links, when we release them. The 2017 Ranger Manual can be
  found in the right sidebar of <a href="http://rangers.burningman.org">http://rangers.burningman.org</a>
</p>
<p>
  Please note that due to a limited number of spaces for new Rangers, only the
  first 158 applicants to complete the manual review will have Alpha shifts
  slots reserved for them. Everyone else will go on a waiting list in the order
  in which they complete the manual review. See the note at the bottom of the
  page for what happens if you get wait-listed.
</p>
<p>
  4. <b>Sign up for a training.</b> Once you have completed the other steps, go to “Me”
  in the black menu bar, select “Schedule/Signup”, and pick a training that
  works for you.
</p>
<p>
  Ranger Training locations are posted at
  <a href="http://rangers.burningman.org/training">http://rangers.burningman.org/training</a>
  and will continue to be updated through the end of May, as new trainings are
  announced. Stay tuned to New Volunteer Announce as new trainings are posted
  Please sign up for a training by June 15th. <span style="color: red">There are no walk-ins for
  trainings,</span> so make sure you sign up in the Clubhouse. Please sign up for only
  ONE training so as not to take away spaces from others!
</p>
<p style="font-size: 1.1em; font-weight: bold;">
  Note that to sign up for an Alpha shift, you will need to first pass training.
  Taking the Review AND uploading a BMID photo are prerequisites for Training,
  and Training is a prerequisite for Mentoring.
</p>
<p>
  Waitlisted Applicants:
</p>
<p>
  If you are the 159th or later applicant to have completed the manual review,
  you will be put on a waiting list, ordered by manual review completion. If
  people ahead of you drop out of the process, the Volunteer Coordinators will
  take you off the waitlist and let you know what the next steps are.
</p>
